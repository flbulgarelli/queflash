function generateCards(verbs: any[], category: string) {
  return verbs.flatMap(it => [
    { item: it.verbo, question: "eu _", answer: "eu " + it.eu },
    { item: it.verbo, question: "ele _", answer: "ele " + it.ele },
    { item: it.verbo, question: "eles _", answer: "eles " + it.eles },
    { item: it.verbo, question: "nós _", answer: "nós " + it['nós'] },
  ]).map(it => ({ ...it, category: category }))
}

